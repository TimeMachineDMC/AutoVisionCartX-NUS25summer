async function sendPendingOrdersToRobot() {
    // Ëé∑ÂèñÂΩìÂâçÂïÜÂÆ∂ÁöÑÂæÖÂ§ÑÁêÜËÆ¢Âçï
    const sellerProducts = products.filter(p => p.owner === currentUser);
    const sellerProductNames = sellerProducts.map(p => p.name);
    const pendingOrders = allOrders.filter(order => 
        sellerProductNames.includes(order.product) && order.status === 'pending'
    );
    
    if (pendingOrders.length === 0) {
        showNotification('No pending orders to send', 'warning');
        return;
    }
    
    // Ëé∑ÂèñÁ¨¨‰∏Ä‰∏™ÂæÖÂ§ÑÁêÜËÆ¢ÂçïÁöÑÂïÜÂìÅÂíåÂÆ¢Êà∑‰ø°ÊÅØ
    const targetProduct = pendingOrders[0].product;
    const targetCustomer = pendingOrders[0].customer; // ‰ΩøÁî®ÂÆûÈôÖ‰∏ãÂçïÁöÑÂÆ¢Êà∑
    
    try {
        const response = await fetch('http://localhost:5001/set_order_target', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                product: targetProduct,
                customer: targetCustomer
            })
        });
        
        if (response.ok) {
            const data = await response.json();
            if (data.status === 'success') {
                // Êõ¥Êñ∞ËÆ¢ÂçïÁä∂ÊÄÅ‰∏∫Â∑≤ÂèëÈÄÅ
                pendingOrders[0].status = 'sent';
                localStorage.setItem('allOrders', JSON.stringify(allOrders));
                
                showNotification(`ËÆ¢ÂçïÂ∑≤ÂèëÈÄÅÁªôÊú∫Âô®‰∫∫: ${targetProduct}`);
                loadPendingOrders(); // Âà∑Êñ∞ÊòæÁ§∫
            } else if (data.status === 'warning') {
                showNotification('ËÆ¢ÂçïÂèëÈÄÅÂ§±Ë¥•Ôºå‰ΩÜÂ∑≤ËÆ∞ÂΩï', 'warning');
            } else {
                showNotification(`ÂèëÈÄÅÂ§±Ë¥•: ${data.message}`, 'error');
            }
        } else {
            const errorData = await response.json();
            showNotification(`ÂèëÈÄÅÂ§±Ë¥•: ${errorData.message}`, 'error');
        }
    } catch (error) {
        console.error('MQTTÊúçÂä°ËøûÊé•Â§±Ë¥•:', error);
        showNotification('MQTTÊúçÂä°ËøûÊé•Â§±Ë¥•', 'error');
    }
}

