function addVoiceOrderToCart(order) {
    console.log('üîç Processing voice order:', order);
    console.log('üì¶ Available products:', products.map(p => p.name));
    
    // Êô∫ËÉΩÂåπÈÖçÂïÜÂìÅÂêçÁß∞
    let product = null;
    
    // È¶ñÂÖàÂ∞ùËØïÁ≤æÁ°ÆÂåπÈÖçÔºà‰∏çÂå∫ÂàÜÂ§ßÂ∞èÂÜôÔºâ
    product = products.find(p => p.name.toLowerCase() === order.product.toLowerCase());
    
    // Â¶ÇÊûúÊ≤°ÊâæÂà∞ÔºåÂ∞ùËØïÈÉ®ÂàÜÂåπÈÖçÔºàÂåÖÂê´ÂÖ≥ÈîÆËØçÔºâ
    if (!product) {
        product = products.find(p => 
            order.product.toLowerCase().includes(p.name.toLowerCase()) ||
            p.name.toLowerCase().includes(order.product.toLowerCase())
        );
    }
    
    // Â¶ÇÊûúËøòÊ≤°ÊâæÂà∞ÔºåÂ∞ùËØïÂÖ≥ÈîÆËØçÂåπÈÖç
    if (!product) {
        const keywords = {
            'banana': 'Banana',
            'bananas': 'Banana',
            'fresh': 'Banana',
            'book': 'Book',
            'books': 'Book'
        };
        
        for (const [keyword, productName] of Object.entries(keywords)) {
            if (order.product.toLowerCase().includes(keyword)) {
                product = products.find(p => p.name === productName);
                if (product) break;
            }
        }
    }
    
    if (!product) {
        console.log('‚ùå Product not found:', order.product);
        showNotification(`Product "${order.product}" not found in catalog. Available: ${products.map(p => p.name).join(', ')}`, 'error');
        return;
    }

    console.log('‚úÖ Found product:', product);

    // Ê£ÄÊü•Áé∞ÊúâË¥≠Áâ©ËΩ¶‰∏≠ÊòØÂê¶Â∑≤ÊúâËØ•ÂïÜÂìÅ
    const existingItem = selectedProducts.find(item => item.name === product.name);
    
    if (existingItem) {
        existingItem.quantity += order.quantity;
        showNotification(`Updated quantity: ${existingItem.quantity} x ${product.name}`);
    } else {
        selectedProducts.push({
            name: product.name, // ‰ΩøÁî®Ê≠£Á°ÆÁöÑÂïÜÂìÅÂêçÁß∞ÔºàÈ¶ñÂ≠óÊØçÂ§ßÂÜôÔºâ
            price: product.price,
            owner: product.owner,
            quantity: order.quantity
        });
        showNotification(`Added to cart: ${order.quantity} x ${product.name}`);
    }

    // ‰øùÂ≠òÂà∞Êú¨Âú∞Â≠òÂÇ®
    localStorage.setItem('selectedProducts', JSON.stringify(selectedProducts));
    
    // Âà∑Êñ∞ÊòæÁ§∫
    displayCart();
    loadProducts(); // ÈáçÊñ∞Ê∏≤Êüì‰∫ßÂìÅÂç°Áâá‰ª•Êõ¥Êñ∞ÊåâÈíÆÁä∂ÊÄÅ
}

